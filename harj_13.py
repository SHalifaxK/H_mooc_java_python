'''
Tee ohjelma, joka kysyy käyttäjältä kolme kokonaislukua ja tulostaa niiden summan.
Anna ensimmäinen luku: 5
Anna toinen luku: 2
Anna kolmas luku: 12
Lukujen summa: 19
'''

eka = int(input('Anna ensimmainen luku: '))
toka = int(input('Anna toinen luku: '))
kolmas = int(input('Anna kolmas luku: '))
tulos=eka+toka+kolmas
print('Lukujen summa: '+ str(tulos))
